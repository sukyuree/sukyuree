MAIN	START	1000

	LDA	ZERO
	STA	PRE
	STA	COUNT
	LDA	ONE
	STA	CUR

	
LOOP	LDA	COUNT
	COMP	SIX
	JEQ	FIB
	JLT	FIB
	JGT	END

FIB	JSUB	MAKE
	LDA	CUR
	ADD	PRE
	STA	TEMP
	LDA	CUR
	STA	PRE
	LDA	TEMP
	STA	CUR
	LDA	COUNT
	ADD	ONE
	STA	COUNT
	J	LOOP

MAKE	LDA	CUR
	COMP	ASEN
	JLT	MAKE_I
	JEQ	MAKE_II
	JGT	MAKE_II

MAKE_I	LDA	CUR
	ADD	I
	WD	STDOUT
	LDA	ASEN
	WD	STDOUT
	RSUB


MAKE_II	LDA	CUR
	DIV	ASEN
	STA	T_TEN
	MUL	ASEN
	STA	TT
	LDA	CUR
	SUB	TT
	STA	T_ONE
	LDA	T_TEN
	ADD	I
	WD	STDOUT
	LDA	T_ONE
	ADD	I
	WD	STDOUT
	LDA	ASEN
	WD	STDOUT
	RSUB

END	J	END

STDIN	BYTE	0
STDOUT	BYTE	1

ZERO	WORD	0
SIX	WORD	6
ONE	WORD	1
I	WORD	48
ASEN	WORD	10

TT	RESW	1
T_TEN	RESW	1
T_ONE	RESW	1
TEMP	RESW	1
COUNT	RESW	1
CUR	RESW	1
PRE	RESW	1
