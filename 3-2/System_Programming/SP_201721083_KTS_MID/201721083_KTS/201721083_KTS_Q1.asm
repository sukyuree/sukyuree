MAIN	START	1000

SET	LDA	#0
	STA	T_1
	STA	T_10
	STA	T_100
	LDX	#3
	STCH	BUFFER,X
	LDX	#2
	STCH	BUFFER,X
	LDX	#1
	STCH	BUFFER,X
	LDX	#0
	STCH	BUFFER,X
INPUT_N	TD	STDIN
	JEQ	INPUT_N
	RD	STDIN
	COMP	ASEN
	JEQ	CHECK_1
	STCH	BUFFER,X
	TIX	#3
	JLT	INPUT_N
	JEQ	CHECK_1

CHECK_1	LDX	#1
	LDCH	BUFFER,X
	COMP	#0
	JEQ	CHECK_0
	JLT	CHECK_2

CHECK_0	LDX	#0
	LDCH	BUFFER,X
	COMP	#0
	JLT	STORE_1
	JEQ	SET

STORE_1	LDX	#2
	LDCH	BUFFER,X
	SUB	MK_I
	STA	T_1
	LDX	#1
	LDCH	BUFFER,X
	SUB	MK_I
	MUL	#10
	STA	T_10
	LDX	#0
	LDCH	BUFFER,X
	SUB	MK_I
	MUL	#100
	STA	T_100
	ADD	T_10
	ADD	T_1
	STA	TEMP
	J	U_D_C		

CHECK_2	LDX	#1
	LDCH	BUFFER,X
	COMP	#0
	JGT	STORE_2
	JLT	CHECK_3
STORE_2	LDX	#1
	LDCH	BUFFER,X
	SUB	MK_I
	STA	T_1
	LDX	#0
	LDCH	BUFFER,X
	SUB	MK_I
	MUL	#10
	STA	T_10
	ADD	T_1
	STA	TEMP
	J	U_D_C	

CHECK_3	LDX	#0
	LDCH	BUFFER,X
	STA	TEMP
	J	U_D_C







U_D_C	LDA	TEMP
	COMP	#150
	JEQ	PR_CO
	JLT	PR_UP
	JGT	PR_DOWN

PR_UP	LDA	#85
	WD	STDOUT
	LDA	#80
	WD	STDOUT
	LDA	ASEN
	WD	STDOUT
	J	SET

PR_DOWN	LDA	#68
	WD	STDOUT
	LDA	#79
	WD	STDOUT
	LDA	#87
	WD	STDOUT
	LDA	#78
	WD	STDOUT
	LDA	ASEN
	WD	STDOUT
	J	SET

PR_CO	LDA	#67
	WD	STDOUT
	LDA	#79
	WD	STDOUT
	LDA	#82
	WD	STDOUT
	LDA	#82
	WD	STDOUT
	LDA	#69
	WD	STDOUT
	LDA	#67
	WD	STDOUT
	LDA	#84
	WD	STDOUT
	LDA	ASEN
	WD	STDOUT
	J	END

END	J	END







BUFFER	RESB	4

STDIN	BYTE	0
STDOUT	BYTE	1


ASEN	WORD	10
MK_I	WORD	48

T_1	RESW	1
T_10	RESW	1
T_100	RESW	1
TEMP	RESW	1

































